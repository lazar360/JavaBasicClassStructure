package academy.learnprogramming;

public class RelationalOperators {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;
        int z = 10;

        System.out.println("x < y : " + (x < y));
        System.out.println("x <= y :" + (x <= y));
        System.out.println("x >= z :" + (x >= z));
        System.out.println("x > z :" + (x > z));
    }
}
